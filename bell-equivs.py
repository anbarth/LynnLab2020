# given a 16-vector representing a set of bell states,
# print the tic-tac-toe diagram
def tictactoe(v):
    s = ""
    for c in range(0,4):
        for p in range(0,4):
            # 1 if psi_c^p is in this set, 0 if not
            statePresent = v[c*4 + p]
            if statePresent == 1:
                s += " X "
            else:
                s += " - "
        s += '\n'
    print(s)

def hasSevenOnes(str):
    numOnes = 0
    for c in str:
        if c == '1':
            numOnes += 1
            
    if numOnes == 7:
        return True
    else:
        return False